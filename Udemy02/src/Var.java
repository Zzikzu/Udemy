public class Var {

	public static void main(String[] args) {
		int myNumber; // declare variable, int 32bit value
		myNumber = 88; // initialize variable
		// int myNumber = 88; // declare & initialize variable in one step

		short myShort = 847; // 16bit value
		long myLong = 9797; // 64bit value

		double myDouble = 7.3243; // desatinne cislo
		float myFloat = 3.34f; // short version for double, "f" on end

		char myChar = 'y'; // single character
		boolean myBoolean = false; // true or false

		byte myByte = 127; // 8bit of data

		System.out.println(myNumber);
		System.out.println(myShort);
		System.out.println(myLong);
		System.out.println(myDouble);
		System.out.println(myFloat);
		System.out.println(myChar);
		System.out.println(myBoolean);
		System.out.println(myByte);

	}

}
